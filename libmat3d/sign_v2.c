#include "mat3d.h"

void	sign_v2(t_v2 *s, t_v2 p0, t_v2 p1)
{
	int a;

	s->x = 0;
	s->y = 0;
	a = p1.x - p0.x;
	if (a < 0)
		s->x = -1;
	else if (a > 0)
		s->x = 1;
	a = p1.y - p0.y;
	if (a < 0)
		s->y = -1;
	else if (a > 0)
		s->y = 1;
}

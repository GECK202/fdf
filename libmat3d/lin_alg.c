#include "mat3d.h"

void	mult_vm(t_v4 *d, t_v4 s, t_m4 m)
{
	t_v4 v;

	v.x = s.x * m.f0.x + s.y * m.f0.y + s.z * m.f0.z + s.w * m.f0.w;
	v.y = s.x * m.f1.x + s.y * m.f1.y + s.z * m.f1.z + s.w * m.f1.w;
	v.z = s.x * m.f2.x + s.y * m.f2.y + s.z * m.f2.z + s.w * m.f2.w;
	v.w = s.x * m.f3.x + s.y * m.f3.y + s.z * m.f3.z + s.w * m.f3.w;
	d->x = v.x;
	d->y = v.y;
	d->z = v.z;
	d->w = v.w;
}
